import java.lang.*;

public class PrettyPrinting {
   public static void main(String[] args) {
    float a=453.12345f;
    // f for formatted string
    System.out.printf("Formatted number is: %.2f",a);
    System.out.println();
    System.out.printf("Formatted value of PI:%.2f",Math.PI);
    System.out.println();
    System.out.printf("Hello my name is %s and I live i %s","faizan","delhi"); 
  }

}
